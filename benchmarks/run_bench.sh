#!/usr/bin/env bash
# Single-shot benchmark runner.
#
# Designed to be invoked inside the http-c-build:ubuntu24 image with the
# repository mounted at /work. Builds two equivalent HTTPS servers:
#
#   1. http-c   (this library)        - port 8443
#   2. mongoose (cesanta/mongoose)    - port 8444
#
# Both serve GET / -> "hello\n" with explicit Content-Length and TLS via
# OpenSSL 3.x. Then drives load with `wrk` and writes a Markdown report
# to /work/benchmarks/REPORT.md.
set -euo pipefail

ROOT=/work
BENCH=$ROOT/benchmarks
EXT=$BENCH/external
RESULTS=$BENCH/results
REPORT=$BENCH/REPORT.md
CERT_ROOT=/tmp/http-c-certs

PORT_HTTP_C=8443
PORT_MONGOOSE=8444

# wrk parameters (overridable from environment).
DURATION=${DURATION:-15s}
THREADS=${THREADS:-2}
CONNECTIONS=${CONNECTIONS:-64}

mkdir -p "$EXT" "$RESULTS"

echo "==> Installing wrk + git"
DEBIAN_FRONTEND=noninteractive apt-get update -qq >/dev/null
DEBIAN_FRONTEND=noninteractive apt-get install -y -qq wrk git ca-certificates >/dev/null

echo "==> Generating certs (reusing test_setup harness)"
if [ ! -f "$CERT_ROOT/server/server.cert.pem" ]; then
    bash "$ROOT/.devcontainer/run-tests.sh" >/tmp/setup.log 2>&1 || true
fi

echo "==> Fetching mongoose (single-file)"
if [ ! -f "$EXT/mongoose/mongoose.c" ]; then
    rm -rf "$EXT/mongoose"
    git clone --depth=1 https://github.com/cesanta/mongoose.git "$EXT/mongoose" >/dev/null 2>&1
fi
MG_VER=$(git -C "$EXT/mongoose" describe --tags --always 2>/dev/null || echo unknown)

echo "==> Configuring + building benchmark binaries"
BUILD=$ROOT/build-bench
rm -rf "$BUILD"
cmake -S "$ROOT" -B "$BUILD" -DHTTP_C_BUILD_BENCHMARKS=ON -DCMAKE_BUILD_TYPE=Release >/dev/null
cmake --build "$BUILD" -j"$(nproc)" --target http_c_bench mongoose_bench >/dev/null

HTTP_C_BIN=$BUILD/benchmarks/http_c_bench
MONGOOSE_BIN=$BUILD/benchmarks/mongoose_bench

[ -x "$HTTP_C_BIN" ]   || { echo "build failed: http_c_bench"; exit 1; }
[ -x "$MONGOOSE_BIN" ] || { echo "build failed: mongoose_bench"; exit 1; }

run_one() {
    local label=$1 bin=$2 port=$3 url=$4
    echo
    echo "==================================================================="
    echo "  $label"
    echo "==================================================================="

    BENCH_PORT=$port "$bin" >"$RESULTS/$label.server.log" 2>&1 &
    local pid=$!
    # Wait for the listener to come up (1s is plenty in container).
    sleep 0.5
    if ! kill -0 $pid 2>/dev/null; then
        echo "$label failed to start; log:"
        cat "$RESULTS/$label.server.log" || true
        return 1
    fi

    # Brief warm-up.
    wrk -t1 -c4 -d2s "$url" >/dev/null 2>&1 || true

    wrk -t"$THREADS" -c"$CONNECTIONS" -d"$DURATION" --latency "$url" \
        | tee "$RESULTS/$label.wrk.txt"

    # Neither bench server installs a SIGINT handler; SIGTERM is also not
    # caught, so go straight to SIGKILL after a short grace period to keep
    # the script reproducible and non-interactive.
    kill -TERM $pid 2>/dev/null || true
    for _ in 1 2 3 4 5; do
        kill -0 $pid 2>/dev/null || break
        sleep 0.2
    done
    kill -KILL $pid 2>/dev/null || true
    wait $pid 2>/dev/null || true
}

# mongoose listens on its own URL; we override via env to choose port.
BENCH_URL_M="https://127.0.0.1:$PORT_MONGOOSE"

# http-c bench picks BENCH_PORT from env (BENCH_PORT=8443 default); set it
# explicitly to keep the two servers on distinct ports.
run_one "http_c"   "$HTTP_C_BIN"   "$PORT_HTTP_C"   "https://127.0.0.1:$PORT_HTTP_C/"
BENCH_URL="$BENCH_URL_M" run_one "mongoose" "$MONGOOSE_BIN" "$PORT_MONGOOSE" "https://127.0.0.1:$PORT_MONGOOSE/"

echo
echo "==> Building report at $REPORT"

extract_metric() {
    local file=$1 key=$2
    grep -E "^[[:space:]]*$key" "$file" | head -n1 | awk '{$1=""; print substr($0,2)}'
}

http_c_rps=$(grep -E "^Requests/sec:" "$RESULTS/http_c.wrk.txt"       | awk '{print $2}')
mong_rps=$(grep   -E "^Requests/sec:" "$RESULTS/mongoose.wrk.txt"     | awk '{print $2}')
http_c_xfer=$(grep -E "^Transfer/sec:" "$RESULTS/http_c.wrk.txt"      | awk '{print $2}')
mong_xfer=$(grep   -E "^Transfer/sec:" "$RESULTS/mongoose.wrk.txt"    | awk '{print $2}')
http_c_lat=$(awk '/Latency  /{print $2; exit}' "$RESULTS/http_c.wrk.txt")
mong_lat=$(awk   '/Latency  /{print $2; exit}' "$RESULTS/mongoose.wrk.txt")
http_c_p99=$(awk '/^[[:space:]]*99%[[:space:]]/{print $2; exit}' "$RESULTS/http_c.wrk.txt")
mong_p99=$(awk   '/^[[:space:]]*99%[[:space:]]/{print $2; exit}' "$RESULTS/mongoose.wrk.txt")

KERNEL=$(uname -srm)
CPU=$(grep -m1 'model name' /proc/cpuinfo | sed 's/.*: //')
NCPU=$(nproc)

cat > "$REPORT" <<EOF
# http-c benchmark report

Generated by \`benchmarks/run_bench.sh\` inside the
\`http-c-build:ubuntu24\` Docker image.

## Test setup

- Workload: \`GET /\` returning \`hello\n\` (6 bytes) over HTTPS
- Both servers respond with explicit \`Content-Length\` so wrk can use
  HTTP/1.1 keep-alive on persistent connections.
- TLS: OpenSSL 3.0.13 (server cert is the same self-signed RSA-2048 cert
  used by the http-c test suite, generated by
  \`.devcontainer/run-tests.sh\`).
- Load generator: \`wrk\` ($THREADS threads, $CONNECTIONS connections,
  $DURATION duration), invoked over loopback.
- Container: \`http-c-build:ubuntu24\` (Ubuntu 24.04 LTS, $KERNEL).
- CPU: $CPU ($NCPU cores visible to the container).

## Servers under test

| Server   | Source                                  | I/O model                          |
|----------|-----------------------------------------|------------------------------------|
| http-c   | this repository (commit \`$(git -C "$ROOT" rev-parse --short HEAD)\`) | single-threaded epoll event loop, non-blocking OpenSSL, zero alloc per request |
| mongoose | github.com/cesanta/mongoose ($MG_VER)    | single-threaded \`poll\` event loop, OpenSSL TLS adapter |

> lwIP is **not** included as a runtime peer. It is a TCP/IP stack
> targeted at MMU-less RTOS environments, not a Linux HTTP server: there
> is no native socket(2) backend on Linux, only an experimental TUN/TAP
> simulator (\`contrib/ports/unix\`) that provides a userspace IPv4 stack
> on top of a tap device. Comparing wire-line throughput against a
> kernel-bypass simulator is unfair in either direction. We instead note
> qualitative tradeoffs in the discussion below.

## Results

| Metric          | http-c             | mongoose           |
|-----------------|--------------------|--------------------|
| Requests/sec    | **$http_c_rps**    | **$mong_rps**      |
| Transfer/sec    | $http_c_xfer       | $mong_xfer         |
| Avg latency     | $http_c_lat        | $mong_lat          |
| P99 latency     | $http_c_p99        | $mong_p99          |

Raw \`wrk\` output is checked in alongside this report under
\`benchmarks/results/*.wrk.txt\`.

## Discussion

Both servers run on a single thread and use OpenSSL for the TLS layer, so
this measures *the framing + dispatch overhead* on top of a common TLS
record path rather than crypto throughput. The numbers above should be
read as relative, not absolute.

### http-c characteristics

- Static caller-supplied storage (no malloc/free on the request path).
- Bounded slot count (\`HTTP_SERVER_HOST_CONNECTIONS\`, default 256). When
  every slot is in use the listener is unregistered from epoll until a
  slot frees, providing strict back-pressure.
- Incremental request parser scans only the new bytes per chunk and
  short-circuits once \`Content-Length\` is known.
- Response is serialised in place into a per-slot send buffer; no
  intermediate copies.

### mongoose characteristics

- Linked list of connections; \`mg_mgr_poll\` walks the list each tick.
- Allocates request and response buffers per connection lifecycle.
- TLS adapter is well-tested and matches the OpenSSL setup.

### lwIP (qualitative note)

lwIP shines where http-c and mongoose cannot run at all: the lwIP HTTPD
on a 32 KB MMU-less microcontroller without an OS socket layer. Its
zero-copy PBUF chains and tight memory pools are tuned for that
environment. On Linux, where a real epoll/SO_REUSEPORT kernel stack is
available, lwIP's userspace TCP/IP stack adds latency rather than
removing it; that is why no production Linux server uses it.

## Reproducing

\`\`\`sh
docker run --rm -v \$(pwd):/work -w /work http-c-build:ubuntu24 \\
    bash /work/benchmarks/run_bench.sh
\`\`\`

Override the load shape via environment variables:

\`\`\`sh
DURATION=30s THREADS=4 CONNECTIONS=256 \\
    docker run --rm -e DURATION -e THREADS -e CONNECTIONS \\
        -v \$(pwd):/work -w /work http-c-build:ubuntu24 \\
        bash /work/benchmarks/run_bench.sh
\`\`\`
EOF

echo
echo "==> Done. Report:"
echo "    $REPORT"
